# String lowercase
a='Komal Yadav'
print(a.lower())
print(a.upper())

#true & false
print(a.isupper())
print(a.upper().isupper())
#length of string
print(len(a))

#index of the characters in the string
print(a[2])

# return index of string
print(a.index("l"))
print(a.index("Yadav"))

#replace the string
print(a.replace("Yadav","Kawat"))